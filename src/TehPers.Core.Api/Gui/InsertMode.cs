namespace TehPers.Core.Api.Gui
{
    /// <summary>
    /// Modes of text insertion.
    /// </summary>
    public enum InsertMode
    {
        /// <summary>
        /// Inserts the text at the cursor position.
        /// </summary>
        Insert,

        /// <summary>
        /// Replaces the text at the cursor position.
        /// </summary>
        Replace,
    }
}